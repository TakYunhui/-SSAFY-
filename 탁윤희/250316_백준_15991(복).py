import sys
input = sys.stdin.readline

t = int(input())
# 대칭: 1+..+1, 2+..+2, 3+..+3 => i-2, i-4, i-6
dp = [0] * 100001
# 1~6까지는 적어두고 그 다음부터 dp 점화식 계산
dp[1] = 1 # 1
dp[2] = 2 # 1+1, 2
dp[3] = 2 # 1+1+1, 3
dp[4] = 3 # 1+1+1+1, 1+2+1, 2+2
dp[5] = 3 # 1+1+1+1+1, 2+1+2, 1+3+1
dp[6] = 6 # 1+1+1+1+1+1, 1+1+2+1+1, 1+2+2+1, 2+2+2, 2+1+1+2, 3+3

for i in range(7, 100001):
    dp[i] = (dp[i-2] + +dp[i-4] + dp[i-6]) % 1000000009

for _ in range(t):
    n = int(input())
    print(dp[n])